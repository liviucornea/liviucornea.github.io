
export class CalendarEventTitle {

  month(event: any): string {
    return event.title;
  }

  monthTooltip(event: any): string {
    return event.title;
  }

  week(event: any): string {
    return event.title;
  }

  weekTooltip(event: any): string {
    return event.title;
  }

  day(event: any): string {
    return event.title;
  }
}